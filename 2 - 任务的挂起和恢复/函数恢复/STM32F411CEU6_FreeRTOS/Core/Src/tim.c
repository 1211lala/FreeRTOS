/*微妙延时用*/
void delay_us(TIM_HandleTypeDef* htimx, uint16_t nus)
{
	__HAL_TIM_SetCounter(htimx,0);
	__HAL_TIM_ENABLE(htimx);

	while(__HAL_TIM_GetCounter(htimx) < nus);
	
	__HAL_TIM_DISABLE(htimx);
}
